<#
.SYNOPSIS
Updates the Windows Defender definition to the last version.

.DESCRIPTION
If the device does not have the last version of the Windows Defender malware or spyware definition, it will force an update.

.FUNCTIONALITY
Remediation

.INPUTS
ID  Label                           Description
1   MaximumDelayInSeconds           Maximum random delay set to avoid overloading the network. Provide number of seconds less than 600
2   InternalUpdateServer            If set to 'true' it will update from an internal service such as WSUS or System Center. If set to 'false' it will update directly from Microsoft. Acceptable values are 'true' or 'false'

.RESTRICTIONS
- This script is only compatible with Windows 10 and Windows 11
- Should be used only on devices without any 3rd party security software which replaces Windows Defender, otherwise update process is unavailable

.NOTES
