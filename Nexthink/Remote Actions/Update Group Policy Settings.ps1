param(
    [Parameter(Mandatory = $true)][string]$GroupPolicyTarget,
    [Parameter(Mandatory = $true)][string]$Force,
    [Parameter(Mandatory = $true)][string]$MaximumDelayInSeconds
)

# End of parameters definition

New-Variable -Name 'MAXIMUM_DELAY_TIME' -Value 600 -Option Constant -Scope script

function Add-NexthinkDLL {
    $remoteActionPath = "$env:NEXTHINK\RemoteActions\nxtremoteactions.dll"
    if (-not (Test-Path -Path $remoteActionPath)) { throw 'Nexthink DLL nxtremoteactions.dll not found. ' }

    Add-Type -Path $remoteActionPath
}

function Test-ExecutionContextValid ([string]$Target) {
    $currentIdentity = Get-CurrentIdentity
    if ($currentIdentity -eq 'S-1-5-18' -and $Target -eq 'user' ) {
        throw @"
The script is run in 'local system' context and cannot update user group policy settings.
To update user group policy settings please change remote action execution context to 'current interactive user'.
"@
    }
}

function Get-CurrentIdentity {
    return [Security.Principal.WindowsIdentity]::GetCurrent().User.ToString()
}

function Test-InputParameters ([hashtable]$MainParams) {
    Test-BooleanParameter -Key 'Force' -Value $MainParams['Force']
    Test-GroupPolicyParameter -Key 'GroupPolicyTarget' -Value $MainParams['GroupPolicyTarget']
    Test-ParamInAllowedRange -Key 'MaximumDelayInSeconds' -Value $MainParams['MaximumDelayInSeconds'] -LowerLimit 0 -UpperLimit $MAXIMUM_DELAY_TIME
}

function Test-BooleanParameter ([string]$Key, [string]$Value) {
    if (@('true', 'false') -notcontains $Value.ToLower()) {
        throw "Error on parameter '$Key'. Accepted values are 'true' or 'false'. "
    }
}

function Test-GroupPolicyParameter ([string]$Key, [string]$Value) {
    if (@('user', 'computer') -notcontains $Value.ToLower()) {
        throw "Error on parameter '$Key'. Only accepted values are 'user' or 'computer'. "
    }
}

function Test-ParamInAllowedRange ([string]$Key, [string]$Value, [int]$LowerLimit = 0, [int]$UpperLimit = $MAXIMUM_DELAY_TIME) {
    $intValue = $Value -as [int]
    if ($intValue -eq $null) {
        throw "Error on parameter '$Key'. It is not an integer. "
    }

    if ($intValue -lt $LowerLimit -or $intValue -gt $UpperLimit) {
        throw "Error on parameter '$Key'. It must be an integer in [$LowerLimit, $UpperLimit]. "
    }
}

function Test-SupportedOSVersion {
    $OSVersion = Get-OSVersion
    if (-not ($OSVersion = $OSVersion -as [version])) { throw 'This script could not return OS version. ' }
    if (($OSVersion.Major -ne 10) -and ($OSVersion.Major -ne 6 -or $OSVersion.Minor -ne 1)) {
        throw 'This script is compatible with Windows 10 and Windows 7 only. '
    }
}

function Get-OSVersion {
    return Get-WmiObject -Class Win32_OperatingSystem -Filter 'ProductType = 1' -ErrorAction SilentlyContinue `
        | Select-Object -ExpandProperty Version -ErrorAction SilentlyContinue
}

function Test-DomainMachine {
    try {
        [PSCustomObject]$computerSystemClass = Get-ComputerSystemClass
        [bool]$partOfDomain = $computerSystemClass.PartOfDomain
        [string]$workgroup = $computerSystemClass.Workgroup
    } catch {
        throw 'The script failed because could not test if machine is a domain member. '
    }

    if (-not ($partOfDomain) -or $workgroup -ne '') {
        throw 'This script only works on Machines connected to the domain. '
    }
}

function Get-ComputerSystemClass {
    $computerSystem = Get-WmiObject -Class Win32_ComputerSystem -ErrorAction Stop
    return [PSCustomObject]@{
        'PartOfDomain' = $computerSystem.PartOfDomain
        'Workgroup'    = $computerSystem.Workgroup
    }
}

function Wait-RandomTime ([int]$MaximumDelayInSeconds) {
    if ($MaximumDelayInSeconds -gt 0) {
        $timespan = Get-Random -Minimum 0 -Maximum $MaximumDelayInSeconds
        Start-Sleep -Seconds $timespan
    }
}

function Invoke-GPUpdateCommand ([string]$Target, [string]$Force) {
    $gpArgument = "/c $env:windir\System32\gpupdate /target:$Target"
    if ($Force.ToLower() -eq 'true') { $gpArgument += " /force" }

    [int]$returnCode = Invoke-ConsoleCommand -FilePath $env:COMSPEC -ArgumentList $gpArgument
    return $returnCode
}

function Invoke-ConsoleCommand ([string]$FilePath, [string]$ArgumentList) {
    try {
        $processInfo = New-Object System.Diagnostics.ProcessStartInfo
        $processInfo.FileName = $filePath
        $processInfo.RedirectStandardError = $true
        $processInfo.RedirectStandardOutput = $true
        $processInfo.UseShellExecute = $false
        $processInfo.CreateNoWindow = $false
        $processInfo.Arguments = $argumentList
        $process = New-Object System.Diagnostics.Process
        $process.StartInfo = $processInfo
        $process.Start() | Out-Null
        $process.WaitForExit()
    } catch {
        throw "Error during starting $filePath $argumentList process. "
    }
    $process.ExitCode
}

function Format-OutputMessage ([int]$ReturnCode, [string]$Target) {
    $CapitalizedTarget = $Target[0].ToString().ToUpper() + $Target.Substring(1).ToLower()
    if ($ReturnCode -eq 0) {
        return "$CapitalizedTarget Group Policy settings were applied successfully. "
    } else {
        throw "$CapitalizedTarget Group Policy settings could not be applied successfully. "
    }
}

function Get-LoggedUser {
    try {
        return [string](Invoke-Expression -Command 'whoami')
    } catch {
        throw "Logged user cannot be properly retrieved. "
    }
}

#
# Nexthink Engine update
#
function Update-EngineOutputVariables ([string]$LoggedUser, [string]$GPUpdateResult, [int]$ExitCode) {
    if ($ExitCode -eq 0) {
        # Concatenate group policy update result with 'Execution status details' field
        $host.ui.WriteErrorLine($GPUpdateResult)
    }
    [Nxt]::WriteOutputString("LoggedUser", $LoggedUser)
}

#
# Main script flow
#
$ExitCode = 0

try {
    $ParametersList = $MyInvocation.BoundParameters

    Add-NexthinkDLL
    Test-ExecutionContextValid -Target $GroupPolicyTarget
    Test-SupportedOSVersion
    Test-InputParameters -MainParams $ParametersList
    Test-DomainMachine
    Wait-RandomTime -MaximumDelayInSeconds $MaximumDelayInSeconds

    $GPReturnCode = Invoke-GPUpdateCommand -Target $GroupPolicyTarget -Force $Force
    $GPUpdateResult = Format-OutputMessage -ReturnCode $GPReturnCode -Target $GroupPolicyTarget
    $LoggedUser = Get-LoggedUser
} catch {
    $host.ui.WriteErrorLine($_.ToString())
    $ExitCode = 1
} finally {
    Update-EngineOutputVariables -LoggedUser $LoggedUser -GPUpdateResult $GPUpdateResult -ExitCode $ExitCode
    [Environment]::Exit($ExitCode)
}

